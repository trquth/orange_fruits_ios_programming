import UIKit

extension UIColor {
    
    static var primary: UIColor {
        return UIColor(red: 40/255, green: 116/255, blue: 240/255, alpha: 1.0)
    }
    
    static var secondary: UIColor {
        return UIColor(red: 255/255, green: 80/255, blue: 0/255, alpha: 1.0)
    }
    
    static var disabledColor: UIColor {
        return UIColor(red: 85/255, green: 85/255, blue: 85/225, alpha: 1.0)
    }
    
    static var actionColor: UIColor {
        return UIColor(red: 77/255, green: 177/255, blue: 104/225, alpha: 1.0)
    }
    
}
